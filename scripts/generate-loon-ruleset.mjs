import { promises as fs } from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const sourceDir = path.join(root, 'src', 'rules');
const outputDir = path.join(root, 'dist', 'loon');


async function generate() {
  const entries = await fs.readdir(sourceDir, { withFileTypes: true });
  const sourceFiles = entries
    .filter((entry) => entry.isFile() && entry.name.endsWith('.txt'))
    .map((entry) => entry.name)
    .sort();

  await fs.mkdir(outputDir, { recursive: true });

  for (const fileName of sourceFiles) {
    const sourcePath = path.join(sourceDir, fileName);
    const outputName = `${path.basename(fileName, '.txt')}.list`;
    const outputPath = path.join(outputDir, outputName);

    const raw = await fs.readFile(sourcePath, 'utf8');
    const rules = raw
      .split(/\r?\n/)
      .map((line) => line.replace(/#.*/, '').trim())
      .filter(Boolean);


    const content = [
      '# Auto-generated by scripts/generate-loon-ruleset.mjs',
      `# Source: src/rules/${fileName}`,
      ...rules,
      ''
    ].join('\n');

    await fs.writeFile(outputPath, content, 'utf8');
  }
}

generate().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
